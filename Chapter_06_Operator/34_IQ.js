// compound operators
let x = 10;
x += 5; // x = x + 5
console.log(x);

let y = 20;
y -= 10; // y = y - 10
console.log(y);

let a = 4;
a *= 2; // a = a * 2
console.log(a);

let b = 15;
b /= 3; // b = b / 3
console.log(b);

let c = 5;
c **= 3; // c = c ** 3
console.log(c);

let d = 10;
d %= 3; // d = d % 3    
console.log(d);

let e = 7;
e++; // e = e + 1
console.log(e);

let f = 10;
f--; // f = f - 1
console.log(f);

